    //Producto
	class ProductoVO
	{
		//*******************************************************
		//*******************************************************
		//*******************************************************
		//Atributos de VO 
		private $id;
		private $nombre;
		private $precio;
		private $referencia;
		//*******************************************************
		//*******************************************************
		//*******************************************************
		public function getId()
		{
			return ($this->id);  // . de Java, C# u otros lenguajes es equivalente en PHP -> (viene de C++)
		}

		public function setId($id)  //-->Sobrecarga en el uso de variables el atributo id se llama igual que el parametro id
		{
			$this->id = $id;  //Mediante el uso de this, decirle al compilador de Apache que uno es el atributo de instancia (this->id), diferente al parámetro de la función o método $id
		}
		//*******************************************************
		//*******************************************************
		//*******************************************************
		public function getNombre()
		{
			return ($this->nombre);
		}

		public function setNombre($nombre)
		{
			$this->nombre = $nombre;
		}
		//*******************************************************
		//*******************************************************
		//*******************************************************
		public function getPrecio()
		{
			return ($this->precio);
		}

		public function setPrecio($precio)
		{
			$this->precio = $precio;
		}
		//*******************************************************
		//*******************************************************
		//*******************************************************
		public function getReferencia()
		{
			return ($this->referencia);
		}

		public function setReferencia($referencia)
		{
			$this->referencia = $referencia;
		}
